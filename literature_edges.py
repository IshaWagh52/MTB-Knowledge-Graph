"""Cache XML files locally/Drive"""

def safe_filename(pmcid):
    return str(pmcid).replace("/", "_").replace("\\", "_")

def xml_cache_path(pmcid):
    return os.path.join(XML_CACHE_DIR, f"{safe_filename(pmcid)}.xml")

def fetch_and_cache_pmc_xml(pmcid, sleep_time=0.2):
    path = xml_cache_path(pmcid)

    # If already downloaded, read from cache
    if os.path.exists(path) and os.path.getsize(path) > 100:
        with open(path, "r", encoding="utf-8", errors="ignore") as f:
            return f.read(), "cached"

    url = f"https://www.ebi.ac.uk/europepmc/webservices/rest/{pmcid}/fullTextXML"

    try:
        r = requests.get(url, timeout=90)

        if r.status_code == 200 and r.text.strip():
            with open(path, "w", encoding="utf-8") as f:
                f.write(r.text)

            time.sleep(sleep_time)
            return r.text, "downloaded"

        return None, f"failed_status_{r.status_code}"

    except Exception as e:
        return None, f"error_{type(e).__name__}"

def cache_xml_batch(papers_df, n_papers=300):
    papers = papers_df.head(n_papers).copy()

    log = []

    for _, row in tqdm(papers.iterrows(), total=len(papers)):
        pmcid = row["pmcid"]
        xml_text, status = fetch_and_cache_pmc_xml(pmcid)

        log.append({
            "pmcid": pmcid,
            "status": status,
            "xml_length": len(xml_text) if xml_text else 0,
            "journal": row.get("journal", ""),
            "title": row.get("title", "")
        })

        # save log frequently
        pd.DataFrame(log).to_csv(os.path.join(OUT_DIR, "xml_cache_log.csv"), index=False)

    return pd.DataFrame(log)

