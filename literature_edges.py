def extract_edges_from_cached_xml(papers_df, n_papers=300, save_every=10):
    out_file = os.path.join(OUT_DIR, f"literature_edges_scaled_{n_papers}.csv")
    progress_file = os.path.join(OUT_DIR, f"literature_progress_{n_papers}.csv")

    # Resume existing edges if file exists
    if os.path.exists(out_file):
        literature_df = pd.read_csv(out_file)
        literature_edges = literature_df.to_dict("records")
        processed = set(literature_df["pmcid"].dropna().astype(str))
        print("Resuming. Existing edges:", len(literature_edges))
        print("Already processed PMCIDs:", len(processed))
    else:
        literature_edges = []
        processed = set()

    papers = papers_df.head(n_papers).copy()

    paper_log = []

    for idx, row in tqdm(papers.iterrows(), total=len(papers)):
        pmcid = str(row["pmcid"])

        if pmcid in processed:
            continue

        path = xml_cache_path(pmcid)

        if not os.path.exists(path):
            # Try downloading if missing
            xml_text, status = fetch_and_cache_pmc_xml(pmcid)
        else:
            with open(path, "r", encoding="utf-8", errors="ignore") as f:
                xml_text = f.read()
            status = "cached"

        if not xml_text:
            paper_log.append({
                "pmcid": pmcid,
                "status": "no_xml",
                "edges": 0
            })
            continue

        sentences = extract_sentences_from_xml(xml_text)

        paper_edge_count = 0

        for sent in sentences:
            # sentence quality filter
            if len(sent) > 1200:
                continue

            genes_found = find_genes_in_sentence(sent, gene_aliases)

            if len(genes_found) < 2:
                continue

            # optional: reduce very noisy sentences
            if len(genes_found) > 8:
                continue

            relation = classify_relation(sent)
            score = relation_weights.get(relation, 0.3)

            for i in range(len(genes_found)):
                for j in range(i + 1, len(genes_found)):
                    literature_edges.append({
                        "source_gene": genes_found[i],
                        "relation": relation,
                        "target_gene": genes_found[j],
                        "evidence_source": "literature",
                        "score": score,
                        "confidence_score": score,
                        "is_directed": relation in ["REGULATES", "ACTIVATES", "REPRESSES", "ASSOCIATED_WITH_VIRULENCE"],
                        "evidence_text": sent,
                        "pmid": row.get("pmid", ""),
                        "pmcid": pmcid,
                        "doi": row.get("doi", ""),
                        "paper_title": row.get("title", ""),
                        "journal": row.get("journal", ""),
                        "year": row.get("year", ""),
                        "priority": row.get("priority", "")
                    })
                    paper_edge_count += 1

        processed.add(pmcid)

        paper_log.append({
            "pmcid": pmcid,
            "status": status,
            "sentences": len(sentences),
            "edges": paper_edge_count,
            "journal": row.get("journal", ""),
            "title": row.get("title", "")
        })

        # save progress
        if len(processed) % save_every == 0:
            temp_df = pd.DataFrame(literature_edges)
            temp_df.to_csv(out_file, index=False)

            pd.DataFrame(paper_log).to_csv(progress_file, index=False)

    literature_df = pd.DataFrame(literature_edges)

    # clean
    if not literature_df.empty:
        literature_df = literature_df[literature_df["source_gene"] != literature_df["target_gene"]]
        literature_df = literature_df.drop_duplicates(
            subset=["source_gene", "relation", "target_gene", "pmcid", "evidence_text"]
        )

    literature_df.to_csv(out_file, index=False)
    pd.DataFrame(paper_log).to_csv(progress_file, index=False)

    print("Saved:", out_file)
    print("Final edges:", literature_df.shape)

    return literature_df, pd.DataFrame(paper_log)

