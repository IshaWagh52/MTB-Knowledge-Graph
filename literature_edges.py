abstract_edges = []

for _, row in tqdm(abstract_df.iterrows(), total=len(abstract_df)):
    abstract = row["abstract"]
    sentences = split_sentences(abstract)

    for sent in sentences:
        if len(sent) < 40 or len(sent) > 1000:
            continue

        genes_found = find_genes_in_sentence(sent, gene_aliases)

        if len(genes_found) < 2:
            continue

        # avoid very noisy sentences
        if len(genes_found) > 8:
            continue

        relation = classify_relation(sent)
        score = relation_weights.get(relation, 0.2)

        for i in range(len(genes_found)):
            for j in range(i + 1, len(genes_found)):
                abstract_edges.append({
                    "source_gene": genes_found[i],
                    "relation": relation,
                    "target_gene": genes_found[j],
                    "evidence_source": "literature_abstract",
                    "score": score,
                    "confidence_score": score,
                    "is_directed": relation in directed_relations,
                    "evidence_text": sent,
                    "pmid": row.get("pmid", ""),
                    "pmcid": row.get("pmcid", ""),
                    "doi": row.get("doi", ""),
                    "paper_title": row.get("title", ""),
                    "journal": row.get("journal", ""),
                    "year": row.get("year", "")
                })

abstract_edges_df = pd.DataFrame(abstract_edges)

